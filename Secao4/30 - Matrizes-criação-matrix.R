
# Matrizes usando o matrix()

#Matriz com números de 1 até 20 com 4 linhas e 5 colunas
matrix(1:20, nrow =4 ,ncol=5)


#Matriz com o texto "Brasil" em todas células com 4 linhas e 5 colunas
matrix("Brasil", nrow =4 ,ncol=5)

#Matriz de zeros
matrix(0, nrow =4 ,ncol=5)


#Matriz com números de 1 até 20, 4 linhas, 5 colunas preenchendo o valores por linha
matrix(1:20, nrow =4 ,ncol=5,byrow = T)


#Matriz com números de 1 até 20, 4 linhas, 5 colunas preenchendo o valores por por coluna
matrix(1:40, nrow =4 ,ncol=5,byrow = T)


matrix(1:5, nrow =4 ,ncol=5,byrow = T)


matrix(1:5, nrow =4 ,ncol=5)
